test_integer = 100

print(test_integer + 10)
print(test_integer - 10)
print(test_integer * 10)
print(test_integer / 10)

# 文字列を数値に変換
test_str = '100'

print(int(test_str) + 100)

# 不動小数点
test_str = '100.5'

print(float(test_str) + 100)

test_float = .5

print(test_float)

# 複素素数
test_complex = 100 + 5j

print(test_complex)
print(test_complex.real)
print(test_complex.imag)
